package Animal.Aqua;

import Animal.Animal;

/**
 * 
 * @author Nilesh Patel
 * This class represent the animal which lives in water
 */
public abstract class Aqua extends Animal
{
	/**
	 * 
	 * default constructor set the category of this type of animal to "Aqua"
	 */
	protected Aqua()
	{
		super.category = "Aqua";
	}

}